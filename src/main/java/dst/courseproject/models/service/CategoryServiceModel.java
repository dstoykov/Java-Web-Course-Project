package dst.courseproject.models.service;

public class CategoryServiceModel {

    private String id;

    private String name;

    public CategoryServiceModel() {
    }

    public String getId() {
        return this.id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
